public class usuario {
    String id;
    String nome;
    String password;
    String permissao;
    String usuario;
    String telefone;
    String acessa;
    String loga;
    String desloga;
    String utilizaapp;

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getTelefone() {
        return telefone;
    }
    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }
    public String getID() {
        return id;
    }
    public void setID(String id) {
        this.id = id;
    }
    public String getPassword() {
        return password;
    }
    public void setPassword(String password) {
        this.password = password;
    }
    public String getPermissao() {
        return permissao;
    }
    public void setPermissao(String permissao) {
        this.permissao = permissao;
    }
    public String getUsuario() {
        return usuario;
    }
    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }
    public String getAcessa() {
        return acessa;
    }
    public void setAcessa(String acessa) {
        this.acessa = acessa;
    }
    public String getLoga() {
        return loga;
    }
    public void setLoga(String loga) {
        this.loga = loga;
    }
    public String getDesLoga() {
        return desloga;
    }
    public void setDesLoga(String desloga) {
        this.desloga = desloga;
    }
    public String getUtilizaapp() {
        return utilizaapp;
    }
    public void setUtilizaapp(String utilizaapp) {
        this.utilizaapp = utilizaapp;
    }
}

